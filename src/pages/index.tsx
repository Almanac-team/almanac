import { signIn, useSession } from 'next-auth/react';
import Head from 'next/head';
import { Layout } from '~/components/layout';
import {
    createContext,
    useState,
    type ReactElement,
    useEffect,
    useContext,
} from 'react';

const SessionContext = createContext(true);

function MainPage() {
    const session = useContext(SessionContext);

    return (
        <>
            <Head>
                <title>Dashboard</title>
                <meta name="description" content="Generated by create-t3-app" />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <main className="absolute right-0 top-0 flex h-full flex-col">
                {session ? (
                    <div> </div>
                ) : (
                    <div>
                        <button
                            className="rounded-m border-2 border-black px-2 py-1 font-semibold"
                            onClick={() =>
                                void signIn('auth0', { callbackUrl: '/' })
                            }
                        >
                            Sign in
                        </button>
                    </div>
                )}
            </main>
        </>
    );
}

Home.getLayout = function getLayout(page: ReactElement) {
    return <Layout>{page}</Layout>;
};

export default function Home() {
    const { data: sessionData } = useSession();
    const [session, setSession] = useState(false);
    useEffect(() => setSession(sessionData !== null), [sessionData]);

    return (
        <SessionContext.Provider value={session}>
            <MainPage />
        </SessionContext.Provider>
    );
}